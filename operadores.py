# > Operações Matemáticas

numero1 = 42
numero2 = 2

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2) # divisão inteira
print(20 % 2) #modulo
print(2**3)

